import math

# Funções básicas:

print(abs(-15)) #valor absoluto
print(pow(3,3)) #função de exponenciação
print(max(1,5,-55,15,18)) #busca o valor máximo
print(min(5,1,29,-80)) #busca o valor mínimo 
print(round(8.8)) #arredondamento (abaixo de .5 vai ser menos e acima de .5 vai ser mais)

#Funções da biblioteca de matemática:

print(math.floor(8.8)) #'floor' arredonda para baixo
print(math.ceil(8.9999999)) #'ceil' arredonda para cima
print(math.sqrt(9)) # raiz quadrada